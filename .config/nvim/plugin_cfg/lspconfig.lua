    local function buf_set_option(...) vim.api.nvim_buf_set_option(bufnr, ...) end


end


nvim_lsp.tsserver.setup {
    on_attach = on_attach,
}

nvim_lsp.rust_analyzer.setup {
    on_attach = on_attach,
    settings = {
    ["rust-analyzer"] = {
      assist = {
        importMergeBehavior = "last",
        importPrefix = "by_self",
      },
      diagnostics = {
